#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned int i;

	i = 0;
	if (n == 0)
		return (0);
	while (i < n - 1 && (((unsigned char *)s1)[i] == ((unsigned char *)s2)[i]
				&& ((unsigned char *)s1)[i] != '\0'))
		i++;
	return (((unsigned char *)s1)[i] - ((unsigned char *)s2)[i]);
}
