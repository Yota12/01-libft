#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	char	*last_occur;

	last_occur = NULL;
	while (*str)
	{
		if (*str == (unsigned char)c)
			last_occur = (char *)str;
		str++;
	}
	if (c == 0)
		last_occur = (char *)str;
	return (last_occur);
}
